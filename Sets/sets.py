
#Sets - Like Hashset no duplicates
cs_courses  = {'History', 'Math', 'Physics', 'CompSci','Math'}
art_courses  = {'History', 'Math', 'Physics', 'Art','Design'}

print(f'Intersection - {cs_courses.intersection(art_courses)}') #common values

print(f'Difference {cs_courses.difference(art_courses)}') #difference

print(f'Union :  - {cs_courses.union(art_courses)}') #union




